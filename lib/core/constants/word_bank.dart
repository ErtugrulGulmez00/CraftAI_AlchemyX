/// Returns today's date (UTC) as a `YYYY-MM-DD` string, used as the
/// Challenge Mode session key and leaderboard date filter.
String todayDateKey() {
  final now = DateTime.now().toUtc();
  final year = now.year.toString().padLeft(4, '0');
  final month = now.month.toString().padLeft(2, '0');
  final day = now.day.toString().padLeft(2, '0');
  return '$year-$month-$day';
}

/// Returns today's Challenge Mode target word for [language] (UTC-based,
/// deterministic - both languages pick the same index for the same day).
String todaysChallengeWord(GameLanguage language) {
  final now = DateTime.now().toUtc();
  final startOfYear = DateTime.utc(now.year, 1, 1);
  final dayOfYear = now.difference(startOfYear).inDays;
  final bank = switch (language) {
    GameLanguage.turkishV2 => dailyWordBankTr,
    GameLanguage.german => dailyWordBankDe,
    GameLanguage.spanish => dailyWordBankEs,
    GameLanguage.portuguese => dailyWordBankPt,
    _ => dailyWordBank,
  };
  return bank[dayOfYear % bank.length];
}
